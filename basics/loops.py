# LOOPS IN PYTHON

# FOR LOOP
for i in range(5):
    print("i =", i)

# WHILE LOOP
n = 0
while n < 3:
    print("n =", n)
    n += 1

# LOOPING THROUGH A LIST
fruits = ["apple", "banana", "mango"]
for fruit in fruits:
    print(fruit)

# REMEMBER:
# - for loops use `in` with range or iterable
# - while loops repeat until the condition is false
