# DATA TYPES IN PYTHON

# Basic types
text = "Hello"          # str
number = 42             # int
decimal = 3.14          # float
flag = True             # bool

# Collection types
my_list = [1, 2, 3]         # list
my_tuple = (1, 2, 3)        # tuple
my_set = {1, 2, 3}          # set
my_dict = {"a": 1, "b": 2}  # dict

# Type checking
print(type(text))       # <class 'str'>
print(type(my_dict))    # <class 'dict'>

# REMEMBER:
# - Use type() to know the data type
# - Python is dynamically typed: no need to declare types
