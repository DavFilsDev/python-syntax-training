# OPERATORS IN PYTHON

# Arithmetic Operators
a = 10
b = 3
print(a + b)  # Addition: 13
print(a - b)  # Subtraction: 7
print(a * b)  # Multiplication: 30
print(a / b)  # Division: 3.33...
print(a // b) # Floor division: 3
print(a % b)  # Modulo: 1
print(a ** b) # Exponentiation: 1000

# Comparison Operators
print(a == b)  # False
print(a != b)  # True
print(a > b)   # True
print(a <= b)  # False

# Logical Operators
x = True
y = False
print(x and y) # False
print(x or y)  # True
print(not x)   # False

