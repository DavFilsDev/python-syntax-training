# CLASSES IN PYTHON

# A class is a blueprint for creating objects.

class Person:
    def speak(self):
        print("Hello, I'm a person.")

# Creating an object
p1 = Person()
p1.speak()

# MEMO:
# - Classes group related data and behavior.
# - Use `class` keyword to define a class.
# - Use `object = ClassName()` to create instances.
